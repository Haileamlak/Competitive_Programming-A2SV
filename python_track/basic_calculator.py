num1_str = input("Enter a number sir : ")
num2_str = input("Enter another sir : ")

num1 = float(num1_str)
num2 = float(num2_str)

sum = num1 + num2
diff = num1 - num2
product = num1 * num2
quotient = num1 / num2

print(f"Sum of {num1} and {num2} = {sum}")
print(f"Difference of {num1} and {num2} = {diff}")
print(f"Product of {num1} and {num2} = {product}")
print(f"Quotient of {num1} and {num2} = {quotient}")